package original;


class Strlen {
    /**
     * Return length of given string
     *
     * > strlen("")
     * 0
     * > strlen("abc")
     * 3
     * > strlen("")
     * 0
     * > strlen("x")
     * 1
     * > strlen("asdasnakj")
     * 9
     */
    public static int strlen(String string) {
        return string.length();
    }

}