package scenario3;


import java.io.*;
import java.lang.*;
import java.util.*;
import java.math.*;




class TriangleArea {
    /**
     * Given length of a side and high return area for a triangle.
     * >>> triangle_area(5, 3)
     * 7.5
     *
     * > triangleArea(5, 3)
     * 7.5
     * > triangleArea(2, 2)
     * 2.0
     * > triangleArea(10, 8)
     * 40.0
     */
    public static Double triangleArea(int a, int h) {
        return (a * h) / 2.0;
    }


}