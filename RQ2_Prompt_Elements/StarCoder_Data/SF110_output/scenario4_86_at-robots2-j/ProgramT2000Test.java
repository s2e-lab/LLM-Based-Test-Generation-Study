package net.virtualinfinity.atrobots.computer;


import org.junit.jupiter.api.Test;
import static org.junit.jupiter.api.Assertions.*;

/**
* Test class of {@link Program}.
* It contains ten unit test cases for the {@link Program#createProgramMemory()} method.
*/
class ProgramTest {

    /**
    * Test case for the {@link Program#createProgramMemory()} method.
    * It tests the creation of a read-only memory array with the program code pre-flashed.
    */
    @Test
    void testCreateProgramMemory() {
        short[] programCode = {0x0000, 0x0001, 0x0002, 0x0003, 0x0004, 0x0005, 0x0006, 0x0007, 0x0008, 0x0009};
        Program program = new Program(programCode);
        MemoryArray memory = program.createProgramMemory();
        assertEquals(programCode.length, memory.size());
        for (int i = 0; i < programCode.length; i++) {
            assertEquals(programCode[i], memory.get(i));
        }
    }
}

