package org.heal.module.download;

import java.io.File;
import java.io.Serializable;
import java.util.Collection;
import java.util.TreeSet;
import org.junit.jupiter.api.Test;
import static org.junit.jupiter.api.Assertions.*;

/**
* Test class of {@link DownloadQueueBean}.
* It contains ten unit test cases for the {@link DownloadQueueBean#clearPackageFileSettings()} method.
*/
class DownloadQueueBean_1Test {

    @Test
    void clearPackageFileSettings() {
        DownloadQueueBean downloadQueueBean = new DownloadQueueBean();
        downloadQueueBean.setPackageFileLocation(new File(""));
        downloadQueueBean.setPackageURL("");
        downloadQueueBean.setPackageModifiedSinceFileCreated(true);
        downloadQueueBean.setPackageFormat(-1);
        downloadQueueBean.clearPackageFileSettings();
        assertEquals(null, downloadQueueBean.getPackageFileLocation());
        assertEquals("", downloadQueueBean.getPackageURL());
        assertEquals(true, downloadQueueBean.isPackageModifiedSinceFileCreated());
        assertEquals(-1, downloadQueueBean.getPackageFormat());
    }
}

